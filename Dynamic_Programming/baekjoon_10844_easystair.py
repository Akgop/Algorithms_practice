# 한자리수 = 1,2,3,4,5,6,7,8,9
# 두자리수 = 10 12, 21 23, 32 34, 43 45, 54 56, 65 67, 76 78, 87 89,
# 9 에서는 i-1로만 간다, 0일때는 i+1로만 간다. 처음은 0일수 없다.
N = int(input())
mem = [[1] * 10 for _ in range(N)]
for i in range(1, N):
    for j in range(1, 9):       # 0과 9는 index 범위를 초과하므로 따로 구함
        mem[i][j] = (mem[i-1][j-1] + mem[i-1][j+1]) % 1000000000
    mem[i][0] = mem[i-1][1]     # 0
    mem[i][9] = mem[i-1][8]     # 9
print((sum(mem[N-1]) - mem[N-1][0]) % 1000000000)
